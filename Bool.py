#Boolean: True or False // 1 or 0
bT = True
bF = False
print (str(bT) + ' ' + str(bF))
#Logical Operators: AND (both are true)
bA = True and False
print (str(bA))
#Logical Operators: OR (at least one is true)
bO = True or False
print (str(bO))
#Logical Operators: NOT (they are not what they say)
bNt = not True
bNf = not False
print (bNt)
print (' & ')
print (bNf)
