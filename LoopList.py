#Create a list to test
list = ['Element 1', 'Element 2', 'Element 3']
#Any name could be used in the for instead of "element"
for element in list :
	print (element)
#To print all characters, one by one, in the "Chain" word
for letter in "Chain" :
	print (letter)
#You can play with upper case and lower cases as well
print ('Chain in upper cases')
for letter in "Chain" :
	print (letter.upper())
print ('Chain in lower cases')
for letter in "Chain" :
	print (letter.lower())