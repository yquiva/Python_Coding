#These Relational Operators allows us to compare elements by returning a Boolean value.
v = 4
a = 5
c = 5
#The "==" is used to check if a value is equal to another value.
r1 = c == v
r2 = c == a
print (r1)
print (r2)
#The "!=" is used to check if a value is different to another value.
r3 = c != v
r4 = c != a
print (r3)
print (r4)
#Mathematical Relational Operators works in the same way for programming as they do on a Match
r5 = c < v
r6 = c > a
r7 = v <= a
r8 = a >= c
print (r5)
print (r6)
print (r7)
print (r8)